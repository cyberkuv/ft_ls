#include "ft_ls.h"

void	inval_opt(char x)
{
	ft_putstr_fd("\033[32;1mft_ls: illegal option --\033[32;0m", 2);
	ft_putchar_fd(x, 2);
	ft_putstr_fd("\n\033[32;1musage: ft_ls [-Rrlta] [file ..]\033[32;0m\n", 2);
	exit(EXIT_FAILURE);
}
